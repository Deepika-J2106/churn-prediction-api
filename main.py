from fastapi import FastAPI
from pydantic import BaseModel
import joblib
import pandas as pd
import numpy as np

# Initialize the FastAPI app
app = FastAPI(
    title="Customer Churn Prediction API",
    description="Predicts churn probability for telecom customers using Random Forest",
    version="1.0.0"
)

# Load model and feature columns when server starts
model = joblib.load('churn_model.pkl')
feature_columns = joblib.load('feature_columns.pkl')

# Define what input data looks like
class CustomerData(BaseModel):
    tenure: int
    MonthlyCharges: float
    TotalCharges: float
    Contract_One_year: int      # 1 if one-year contract, else 0
    Contract_Two_year: int      # 1 if two-year contract, else 0
    InternetService_Fiber_optic: int
    InternetService_No: int
    PaymentMethod_Credit_card_automatic: int
    PaymentMethod_Electronic_check: int
    PaymentMethod_Mailed_check: int

# Root endpoint — health check
@app.get("/")
def home():
    return {
        "message": "Churn Prediction API is running",
        "model": "Random Forest (AUC-ROC: 0.8243)",
        "endpoint": "/predict"
    }

# Prediction endpoint
@app.post("/predict")
def predict_churn(customer: CustomerData):
    # Convert input to DataFrame
    input_dict = customer.dict()
    input_df = pd.DataFrame([input_dict])

    # Add missing columns (set to 0 — not provided features default to 0)
    for col in feature_columns:
        if col not in input_df.columns:
            input_df[col] = 0

    # Reorder columns to match training order exactly
    input_df = input_df[feature_columns]

    # Predict probability
    churn_probability = model.predict_proba(input_df)[0][1]
    churn_prediction = int(churn_probability >= 0.35)  # your tuned threshold

    return {
        "churn_probability": round(float(churn_probability), 4),
        "churn_prediction": churn_prediction,
        "risk_level": "High" if churn_probability >= 0.6 else "Medium" if churn_probability >= 0.35 else "Low",
        "threshold_used": 0.35,
        "message": "Customer likely to churn — trigger retention campaign" if churn_prediction == 1 else "Customer likely to stay"
    }

# Batch prediction endpoint
@app.post("/predict_batch")
def predict_batch(customers: list[CustomerData]):
    results = []
    for customer in customers:
        input_dict = customer.dict()
        input_df = pd.DataFrame([input_dict])
        for col in feature_columns:
            if col not in input_df.columns:
                input_df[col] = 0
        input_df = input_df[feature_columns]
        prob = model.predict_proba(input_df)[0][1]
        pred = int(prob >= 0.35)
        results.append({
            "churn_probability": round(float(prob), 4),
            "churn_prediction": pred,
            "risk_level": "High" if prob >= 0.6 else "Medium" if prob >= 0.35 else "Low"
        })
    return {"predictions": results, "total_customers": len(results)}

